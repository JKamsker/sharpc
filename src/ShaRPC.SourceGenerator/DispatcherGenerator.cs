using System.Collections.Generic;
using System.Text;
using System.Threading;

namespace ShaRPC.SourceGenerator;

/// <summary>
/// Generates server dispatcher classes for ShaRPC services. The dispatcher's
/// <c>DispatchAsync</c> is itself async, but it calls the user's service method using
/// the exact shape declared on the interface (sync vs async, with or without a
/// <see cref="System.Threading.CancellationToken"/>). All emitted type references are
/// fully qualified. When a service method's return type is itself a
/// <c>[ShaRpcService]</c> interface the dispatcher registers the returned instance with
/// the per-connection <see cref="ShaRPC.Core.Server.IInstanceRegistry"/> and serializes a
/// <see cref="ShaRPC.Core.Protocol.ServiceHandle"/> in its place. Every dispatcher also
/// emits a <c>DispatchOnInstanceAsync</c> override that resolves a registered instance
/// and runs the same switch against it — so any service may be reached as a sub-service.
/// </summary>
internal static class DispatcherGenerator
{
    public static string Generate(ServiceModel service, CancellationToken ct = default)
    {
        var sb = new StringBuilder();
        var dispatcherName = NamingHelpers.StripInterfacePrefix(service.InterfaceName) + "Dispatcher";
        var qualifiedInterface = QualifyServiceType(service, service.InterfaceName);

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();

        if (!string.IsNullOrEmpty(service.Namespace))
        {
            sb.AppendLine($"namespace {IdentifierHelpers.EscapeNamespace(service.Namespace)}");
            sb.AppendLine("{");
        }

        sb.AppendLine("    /// <summary>");
        sb.AppendLine($"    /// Server dispatcher for {service.InterfaceName}.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine($"    public sealed class {dispatcherName} : global::ShaRPC.Core.Server.IServiceDispatcher");
        sb.AppendLine("    {");
        sb.AppendLine($"        private readonly {qualifiedInterface} _service;");
        sb.AppendLine();
        sb.AppendLine($"        public {dispatcherName}({qualifiedInterface} service)");
        sb.AppendLine("        {");
        sb.AppendLine("            _service = service ?? throw new global::System.ArgumentNullException(nameof(service));");
        sb.AppendLine("        }");
        sb.AppendLine();
        sb.AppendLine($"        public string ServiceName => \"{service.ServiceName}\";");

        EmitDispatchAsync(sb, service, qualifiedInterface, isInstanceScoped: false, ct);
        EmitDispatchAsync(sb, service, qualifiedInterface, isInstanceScoped: true, ct);

        sb.AppendLine("    }");

        if (!string.IsNullOrEmpty(service.Namespace))
        {
            sb.AppendLine("}");
        }

        return sb.ToString();
    }

    private static string QualifyServiceType(ServiceModel service, string typeName) =>
        IdentifierHelpers.QualifyTypeName(service.Namespace, typeName);

    /// <summary>
    /// Emits either <c>DispatchAsync</c> (singleton case) or <c>DispatchOnInstanceAsync</c>
    /// (instance case). Both share the same switch body — only the receiver expression
    /// differs (<c>_service</c> vs the registry-resolved instance).
    /// </summary>
    private static void EmitDispatchAsync(
        StringBuilder sb,
        ServiceModel service,
        string qualifiedInterface,
        bool isInstanceScoped,
        CancellationToken ct)
    {
        sb.AppendLine();
        if (isInstanceScoped)
        {
            sb.AppendLine("        public async global::System.Threading.Tasks.Task DispatchOnInstanceAsync(string instanceId, string method, global::System.ReadOnlyMemory<byte> payload, global::ShaRPC.Core.Serialization.ISerializer serializer, global::ShaRPC.Core.Server.IInstanceRegistry registry, global::System.Buffers.IBufferWriter<byte> output, global::System.Threading.CancellationToken ct = default)");
            sb.AppendLine("        {");
            sb.AppendLine("            await DispatchOnInstanceAsync(instanceId, method, payload, serializer, registry, output, global::ShaRPC.Core.Streaming.RpcStreamingContext.Disabled, ct);");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("#pragma warning disable CS1998");
            sb.AppendLine("        public async global::System.Threading.Tasks.Task DispatchOnInstanceAsync(string instanceId, string method, global::System.ReadOnlyMemory<byte> payload, global::ShaRPC.Core.Serialization.ISerializer serializer, global::ShaRPC.Core.Server.IInstanceRegistry registry, global::System.Buffers.IBufferWriter<byte> output, global::ShaRPC.Core.Streaming.IRpcStreamingContext streaming, global::System.Threading.CancellationToken ct = default)");
        }
        else
        {
            sb.AppendLine("        public async global::System.Threading.Tasks.Task DispatchAsync(string method, global::System.ReadOnlyMemory<byte> payload, global::ShaRPC.Core.Serialization.ISerializer serializer, global::ShaRPC.Core.Server.IInstanceRegistry registry, global::System.Buffers.IBufferWriter<byte> output, global::System.Threading.CancellationToken ct = default)");
            sb.AppendLine("        {");
            sb.AppendLine("            await DispatchAsync(method, payload, serializer, registry, output, global::ShaRPC.Core.Streaming.RpcStreamingContext.Disabled, ct);");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("#pragma warning disable CS1998");
            sb.AppendLine("        public async global::System.Threading.Tasks.Task DispatchAsync(string method, global::System.ReadOnlyMemory<byte> payload, global::ShaRPC.Core.Serialization.ISerializer serializer, global::ShaRPC.Core.Server.IInstanceRegistry registry, global::System.Buffers.IBufferWriter<byte> output, global::ShaRPC.Core.Streaming.IRpcStreamingContext streaming, global::System.Threading.CancellationToken ct = default)");
        }
        sb.AppendLine("#pragma warning restore CS1998");
        sb.AppendLine("        {");

        string receiver;
        if (isInstanceScoped)
        {
            sb.AppendLine($"            if (!registry.TryGet(\"{service.ServiceName}\", instanceId, out var __obj) || __obj is not {qualifiedInterface} __inst)");
            sb.AppendLine("            {");
            sb.AppendLine($"                throw new global::ShaRPC.Core.Exceptions.ShaRpcNotFoundException(\"Instance '\" + instanceId + \"' not found for service '{service.ServiceName}'.\", global::ShaRPC.Core.Exceptions.ShaRpcNotFoundException.NotFoundKind.Instance);");
            sb.AppendLine("            }");
            receiver = "__inst";
        }
        else
        {
            receiver = "_service";
        }

        sb.AppendLine("            switch (method)");
        sb.AppendLine("            {");

        foreach (var m in service.Methods.Array)
        {
            ct.ThrowIfCancellationRequested();
            GenerateDispatchCase(sb, service, m, receiver, ct);
        }

        sb.AppendLine("                default:");
        sb.AppendLine($"                    throw new global::ShaRPC.Core.Exceptions.ShaRpcNotFoundException(\"Method '\" + method + \"' not found on service '{service.ServiceName}'.\", global::ShaRPC.Core.Exceptions.ShaRpcNotFoundException.NotFoundKind.Method);");
        sb.AppendLine("            }");
        sb.AppendLine("        }");
    }

    private static void GenerateDispatchCase(
        StringBuilder sb,
        ServiceModel service,
        MethodModel method,
        string receiver,
        CancellationToken ct)
    {
        // Methods whose shape we cannot marshal (ref/in/out parameters) get no switch case;
        // an inbound call for them falls through to the default ShaRpcNotFoundException
        // branch, which is the same behaviour as calling an unknown method name.
        if (method.UnsupportedReason is not null)
        {
            return;
        }

        sb.AppendLine($"                case \"{method.RpcName}\":");
        sb.AppendLine("                {");

        var requestParameters = GetRequestParameters(method.Parameters, ct);
        if (requestParameters.Count == 1)
        {
            var wireType = ProxyGenerationHelpers.GetWireType(requestParameters[0]);
            sb.AppendLine($"                    var arg = serializer.Deserialize<{wireType}>(payload);");
        }
        else if (requestParameters.Count > 1)
        {
            var tupleTypes = new StringBuilder();
            for (var i = 0; i < requestParameters.Count; i++)
            {
                ct.ThrowIfCancellationRequested();

                if (i > 0) tupleTypes.Append(", ");
                tupleTypes.Append(ProxyGenerationHelpers.GetWireType(requestParameters[i]));
            }

            sb.AppendLine($"                    var args = serializer.Deserialize<({tupleTypes})>(payload);");
        }

        var argumentExpressions = new string[method.Parameters.Count];
        var argumentRequestIndex = 0;
        for (var i = 0; i < method.Parameters.Count; i++)
        {
            ct.ThrowIfCancellationRequested();

            var parameter = method.Parameters[i];
            if (parameter.IsCancellationToken)
            {
                argumentExpressions[i] = "ct";
                continue;
            }

            argumentRequestIndex++;
            var source = requestParameters.Count == 1
                ? "arg"
                : "args.Item" + argumentRequestIndex;
            if (parameter.StreamKind == ParameterStreamKind.None)
            {
                argumentExpressions[i] = source;
                continue;
            }

            var local = ProxyGenerationHelpers.UniqueGeneratedLocalName(
                method.Parameters,
                "__sharpc_arg" + argumentRequestIndex,
                ct);
            sb.AppendLine($"                    var {local} = {BuildStreamingArgument(parameter, source)};");
            argumentExpressions[i] = local;
        }

        var argList = new StringBuilder();
        for (var i = 0; i < method.Parameters.Count; i++)
        {
            ct.ThrowIfCancellationRequested();

            if (i > 0) argList.Append(", ");
            argList.Append(argumentExpressions[i]);
        }

        var target = method.RequiresDispatcherReceiverCast
            ? $"(({method.ExplicitImplementationType}){receiver})"
            : receiver;
        var call = $"{target}.{method.Name}({argList})";

        switch (method.ReturnKind)
        {
            case MethodReturnKind.Void:
                sb.AppendLine($"                    {call};");
                sb.AppendLine("                    return;");
                break;

            case MethodReturnKind.Sync:
                sb.AppendLine($"                    var result = {call};");
                sb.AppendLine("                    serializer.Serialize(output, result);");
                sb.AppendLine("                    return;");
                break;

            case MethodReturnKind.Task:
            case MethodReturnKind.ValueTask:
                sb.AppendLine($"                    await {call};");
                sb.AppendLine("                    return;");
                break;

            case MethodReturnKind.TaskOf:
            case MethodReturnKind.ValueTaskOf:
                sb.AppendLine($"                    var result = await {call};");
                sb.AppendLine("                    serializer.Serialize(output, result);");
                sb.AppendLine("                    return;");
                break;

            case MethodReturnKind.TaskOfSubService:
            case MethodReturnKind.ValueTaskOfSubService:
            {
                // Sub-service return: don't serialize the live instance; instead register
                // it with the per-connection registry and ship back a ServiceHandle the
                // client can wrap in a sub-proxy.
                var info = method.SubService!;
                sb.AppendLine($"                    var __sub = await {call};");
                if (info.AllowsNull)
                {
                    sb.AppendLine("                    if (__sub is null)");
                    sb.AppendLine("                    {");
                    sb.AppendLine("                        serializer.Serialize<global::ShaRPC.Core.Protocol.ServiceHandle?>(output, null);");
                    sb.AppendLine("                        return;");
                    sb.AppendLine("                    }");
                }
                sb.AppendLine("                    string __subId;");
                sb.AppendLine("                    try");
                sb.AppendLine("                    {");
                sb.AppendLine($"                        __subId = registry.Register(\"{info.ServiceName}\", __sub);");
                sb.AppendLine("                    }");
                sb.AppendLine("                    catch");
                sb.AppendLine("                    {");
                sb.AppendLine("                        try");
                sb.AppendLine("                        {");
                sb.AppendLine("                            if (__sub is global::System.IAsyncDisposable __ad)");
                sb.AppendLine("                            {");
                sb.AppendLine("                                await __ad.DisposeAsync().ConfigureAwait(false);");
                sb.AppendLine("                            }");
                sb.AppendLine("                            else if (__sub is global::System.IDisposable __d)");
                sb.AppendLine("                            {");
                sb.AppendLine("                                __d.Dispose();");
                sb.AppendLine("                            }");
                sb.AppendLine("                        }");
                sb.AppendLine("                        catch");
                sb.AppendLine("                        {");
                sb.AppendLine("                            // Best-effort cleanup of the orphaned sub-service: a faulting disposer must");
                sb.AppendLine("                            // not replace the original registration failure that is about to be rethrown.");
                sb.AppendLine("                        }");
                sb.AppendLine("                        throw;");
                sb.AppendLine("                    }");
                sb.AppendLine("                    try");
                sb.AppendLine("                    {");
                sb.AppendLine($"                        serializer.Serialize(output, new global::ShaRPC.Core.Protocol.ServiceHandle {{ ServiceName = \"{info.ServiceName}\", InstanceId = __subId }});");
                sb.AppendLine("                    }");
                sb.AppendLine("                    catch");
                sb.AppendLine("                    {");
                sb.AppendLine($"                        registry.Release(\"{info.ServiceName}\", __subId);");
                sb.AppendLine("                        throw;");
                sb.AppendLine("                    }");
                sb.AppendLine("                    return;");
                break;
            }

            case MethodReturnKind.AsyncEnumerable:
            case MethodReturnKind.Stream:
            case MethodReturnKind.Pipe:
                sb.AppendLine($"                    var result = {call};");
                sb.AppendLine("                    streaming.SetResponse(result);");
                sb.AppendLine("                    return;");
                break;

            case MethodReturnKind.TaskOfAsyncEnumerable:
            case MethodReturnKind.ValueTaskOfAsyncEnumerable:
            case MethodReturnKind.TaskOfStream:
            case MethodReturnKind.ValueTaskOfStream:
            case MethodReturnKind.TaskOfPipe:
            case MethodReturnKind.ValueTaskOfPipe:
                sb.AppendLine($"                    var result = await {call};");
                sb.AppendLine("                    streaming.SetResponse(result);");
                sb.AppendLine("                    return;");
                break;
        }

        sb.AppendLine("                }");
    }

    private static string BuildStreamingArgument(ParameterModel parameter, string source) =>
        parameter.StreamKind switch
        {
            ParameterStreamKind.Stream => $"streaming.GetStream({source})",
            ParameterStreamKind.Pipe => $"streaming.GetPipe({source})",
            ParameterStreamKind.AsyncEnumerable => $"streaming.GetAsyncEnumerable<{parameter.StreamItemType}>({source})",
            _ => source,
        };

    private static List<ParameterModel> GetRequestParameters(
        EquatableArray<ParameterModel> parameters,
        CancellationToken ct)
    {
        var requestParameters = new List<ParameterModel>();
        foreach (var p in parameters.Array)
        {
            ct.ThrowIfCancellationRequested();

            if (!p.IsCancellationToken)
            {
                requestParameters.Add(p);
            }
        }

        return requestParameters;
    }
}
